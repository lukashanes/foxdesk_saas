#!/usr/bin/env sh
set -eu

ROOT_DIR="$(CDPATH= cd -- "$(dirname -- "$0")/.." && pwd)"

if [ -n "${PHP_BIN:-}" ]; then
    exec "$PHP_BIN" "$@"
fi

if command -v php >/dev/null 2>&1; then
    exec php "$@"
fi

if command -v docker >/dev/null 2>&1 && docker image inspect php:8.2-cli >/dev/null 2>&1; then
    ENV_ARGS=""
    for ENV_NAME in \
        APNS_AUTH_KEY APNS_AUTH_KEY_PATH APNS_BUNDLE_ID APNS_KEY_ID APNS_TEAM_ID APNS_TEST_DEVICE_TOKEN APNS_TEST_ENV APNS_TEST_NOTIFICATION_ID APNS_TEST_TICKET_ID APNS_TEST_TYPE \
        APP_DEBUG APP_FALLBACK_HOST APP_HOST APP_MARKETING_HOST APP_NAME APP_URL \
        BILLING_CLOUD_BASE_PRICE_CENTS BILLING_CURRENCY BILLING_ENABLED BILLING_INCLUDED_STORAGE_BYTES BILLING_PAST_DUE_GRACE_DAYS BILLING_STORAGE_OVERAGE_PRICE_CENTS BILLING_TRIAL_DAYS BILLING_TRIAL_GRACE_DAYS \
        CLOUDFLARE_ACCOUNT_ID CLOUDFLARE_EMAIL_API_TOKEN CLOUDFLARE_EMAIL_FROM CLOUDFLARE_EMAIL_FROM_NAME CLOUDFLARE_EMAIL_REPLY_TO \
        DB_HOST DB_NAME DB_PASS DB_PORT DB_ROOT_PASS DB_USER \
        FOXDESK_CLOUDFLARE_EMAIL_INGEST_ENABLED FOXDESK_EMAIL_ALLOW_UNKNOWN_SENDERS FOXDESK_EMAIL_ROUTE_SECRET FOXDESK_EDITION FOXDESK_ENV_FILE FOXDESK_SESSION_DIR FOXDESK_TICKET_EMAIL_DOMAIN FOXDESK_TICKET_EMAIL_LOCAL_PART FOXDESK_UPDATE_CHANNEL \
        IMAP_ALLOW_UNKNOWN_SENDERS IMAP_DENY_EXTENSIONS IMAP_ENABLED IMAP_ENCRYPTION IMAP_FAILED_FOLDER IMAP_FOLDER IMAP_HOST IMAP_MARK_SEEN_ON_SKIP IMAP_MAX_ATTACHMENT_SIZE IMAP_MAX_EMAILS_PER_RUN IMAP_PASSWORD IMAP_PORT IMAP_PROCESSED_FOLDER IMAP_STORAGE_BASE IMAP_USERNAME IMAP_VALIDATE_CERT \
        MAIL_PROVIDER MAX_UPLOAD_SIZE PLATFORM_HOST PLATFORM_URL \
        R2_ACCESS_KEY_ID R2_BUCKET R2_ENDPOINT R2_SECRET_ACCESS_KEY SECRET_KEY STORAGE_DRIVER STRIPE_CANCEL_URL STRIPE_PRICE_CLOUD_BASE STRIPE_PRICE_STORAGE_OVERAGE STRIPE_SECRET_KEY STRIPE_STORAGE_METER_EVENT_NAME STRIPE_SUCCESS_URL STRIPE_TAX_ENABLED STRIPE_TAX_ID_COLLECTION_ENABLED STRIPE_TAX_ID_COLLECTION_REQUIRED STRIPE_WEBHOOK_SECRET TRUST_PROXY; do
        if [ "$(eval "printf '%s' \"\${${ENV_NAME}+set}\"")" = "set" ]; then
            ENV_ARGS="$ENV_ARGS --env $ENV_NAME"
        fi
    done
    # shellcheck disable=SC2086
    exec docker run --rm $ENV_ARGS -v "$ROOT_DIR:/app" -w /app php:8.2-cli php "$@"
fi

echo "PHP CLI was not found."
echo "Install it with: brew install php"
echo "Or pull the Docker fallback with: docker pull php:8.2-cli"
exit 127
